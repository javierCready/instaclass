        {
            $query=trim($request->get('searchText'));
            $unidades=DB::table('unidad')
            ->where('nombre','LIKE','%'.$query.'%')
            ->where('condicion','=','1')
            ->orderBy('id_unidad','desc')
            -paginate(7);
            return view('actividad.unidad.index',["unidades" => $unidades, "searchText"]);
        }
    }

